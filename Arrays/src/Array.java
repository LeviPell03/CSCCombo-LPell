import java.util.Scanner;

public class Array {

	public static void main(String[] args) {
		// Array
		int[] arr = {3,6,4,6};
		int total = 0;
		for (int i = 0; i < arr.length; i++) {
			total += arr.length;
			// Average is "+="
			// Making something green is CTRL + 7
		}
		System.out.println("The average is " + total);
		
	}

}
