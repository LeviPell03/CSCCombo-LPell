
public class LengthAndChar {

	public static void main(String[] args) {
		// Length and Char
		String fruit = "Apple";
		int len = fruit.length();
		char letter = fruit.charAt(len - 1);
		System.out.println(letter);
		
		String empty = "";
		String str = empty + "How are you? /n";
		
		System.out.println(str);
	}

}
